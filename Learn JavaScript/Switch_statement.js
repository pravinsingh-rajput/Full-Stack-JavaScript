// Switch statement

const age = 24;

switch (age) {
  case 20:
    console.log(`You are ${age} year old.`);
    break;
  case 21:
    console.log(`You are ${age} year old.`);
    break;
  case 22:
    console.log(`You are ${age} year old.`);
    break;
  case 23:
    console.log(`You are ${age} year old.`);
    break;
  default:
    console.log(`Your age is not between 21 to 23.`);
}
